    public void displayDataIntoTable() {
        List<History> list = dao.getList();
        DefaultTableModel model = (DefaultTableModel) tblDisplay.getModel();
        model.setNumRows(0);
        Object[] cols = new Object[12];

        for (int i = 0; i < list.size(); i++) {
            cols[0] = list.get(i).getSerials();
            cols[1] = list.get(i).getPassengers();
            cols[2] = list.get(i).getFroms();
            cols[3] = list.get(i).getTos();
            cols[4] = list.get(i).getDates();
            cols[5] = list.get(i).getClassess();
            cols[6] = list.get(i).getSeats();
            cols[7] = list.get(i).getFlightNos();
            cols[8] = list.get(i).getEmails();
            cols[9] = list.get(i).getGenders();
            cols[10] = list.get(i).getPassports();
            cols[11] = list.get(i).getPhones();
            model.addRow(cols);
        }

    }

