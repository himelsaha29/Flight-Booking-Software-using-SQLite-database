    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        // TODO add your handling code here:

        if (txtDate.getText().trim().isEmpty() || txtEmail.getText().trim().isEmpty() || txtGender.getText().trim().isEmpty() || txtNo.getText().trim().isEmpty() || txtPassenger.getText().trim().isEmpty() || txtPassport.getText().trim().isEmpty() || txtPhone.getText().trim().isEmpty() || lblPhoneWarning.isVisible()) {
            lblWarningEdit.setVisible(true);
        } else {

            if ((!(txtEmail.getText().contains("@"))) || (!(txtEmail.getText().contains(".com")))) {
                lblEmailWarning.setVisible(true);
                lblGenderWarning.setVisible(false);
                lblWarningEdit.setVisible(false);
            } else {

                if (((txtGender.getText().trim().equalsIgnoreCase("Male"))) || ((txtGender.getText().trim().equalsIgnoreCase("Female"))) || ((txtGender.getText().trim().equalsIgnoreCase("Other")))) {
                    if (txtPhone.getText().trim().length() != 11) {
                        lblPhoneWarning.setVisible(true);
                    } else {
                        //----------    DATE FUNCTION
                        try {
                            
                            String monthTest = txtDate.getText().substring(txtDate.getText().indexOf("/") + 1, 
                                    txtDate.getText().indexOf("/", txtDate.getText().indexOf("/") + 1));
                            
                            String yearTest = txtDate.getText().substring((txtDate.getText().indexOf("/", 
                                    txtDate.getText().indexOf("/") + 1)) + 1);
                            DateTimeFormatter iso = DateTimeFormatter.ISO_DATE;
                            
                            LocalDate testDate = LocalDate.parse(yearTest + "-" + monthTest + "-" + (txtDate.getText().substring(0, 
                                    txtDate.getText().indexOf("/"))), iso);
                            
                            
                            DateTimeFormatter format = DateTimeFormatter.ofPattern("dd/MM/yyyy");
                            LocalDate ld = LocalDate.parse(txtDate.getText(), format);
                            String dats = ld.format(format);
                            System.out.println(dats);
                            lblDateFormat.setVisible(false);
                        } catch(Exception e) {
                            lblDateFormat.setVisible(true);
                        }
                        if (!lblDateFormat.isVisible()) {
                            
                            try {
                            if ((Long.MAX_VALUE > Long.parseLong(txtPhone.getText().trim()))) {
                                lblWarningEdit.setVisible(false);
                                int YesOrNo = JOptionPane.showConfirmDialog(null, "Current data will be lost. Are you sure you want to update? ", "Confirm Update", JOptionPane.YES_NO_OPTION);

                                if (YesOrNo == 0) {
                                    dao.update(txtPassenger.getText().toUpperCase(), txtDate.getText(), txtEmail.getText().toLowerCase(), txtGender.getText(), txtPassport.getText(), txtPhone.getText(), txtNo.getText());
                                    lblStatus.setText("Update Successful");
                                    lblWarningEdit.setVisible(false);
                                    lblEmailWarning.setVisible(false);
                                    lblGenderWarning.setVisible(false);
                                    lblStatus.setVisible(true);
                                    displayDataIntoTable();
                                } else {

                                }
                            }
                        } catch (Exception g) {
                            lblPhoneWarning.setVisible(true);
                            lblWarningEdit.setVisible(false);
                        }
                        }

                    }

                } else {
                    lblGenderWarning.setVisible(true);
                    lblEmailWarning.setVisible(false);
                    lblWarningEdit.setVisible(false);
                }
            }

        }


    }//GEN-LAST:event_btnEditActionPerformed

